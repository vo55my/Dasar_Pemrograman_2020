package latihan.uts.daspro2020;

import java.util.Scanner;

public class NomorSatu {

	public static void main (String [] args) {
		Scanner sc = new Scanner(System.in);
		int x = sc.nextInt();
		int y = sc.nextInt();
		
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
	}
}