package pertemuan1.daspro2020;

public class LatihanVariabel2 {

	// Kita akan membuktikan bahwa nilai dalam variabel dapat diubah-ubah
	
	public static void main (String [] args) {
		
		String nama = "Mang Oleh";
		
		System.out.println("Namanya adalah " + nama);
		
		nama = "Babang Tamvan";
		nama = "Asep Gorbacep";
		nama = "203040140";
		
		System.out.println("Namanya adalah " + nama);
		
		int angka = 7;
		angka = 10;
		System.out.println("Nilainya adalah " + angka);
		angka = 90_000_000;
		System.out.println("Nilainya adalah " + angka);
	}
}